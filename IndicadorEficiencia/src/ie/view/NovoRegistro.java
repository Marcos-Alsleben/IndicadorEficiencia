    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked

        if ("".equals(txt_pedido.getText())) {
            JOptionPane.showMessageDialog(null, "Número do Pedido Inválido!", "", 2);
        } else {
            if ("".equals(txt_numArtes.getText())) {
                JOptionPane.showMessageDialog(null, "Número de artes Inválido!", "", 2);
            } else {
                if (jd_data.getDate() == null) {
                    JOptionPane.showMessageDialog(null, "Data Iválida!", "", 2);
                } else {
                    if ("".equals(cb_designer.getSelectedItem().toString())) {
                        JOptionPane.showMessageDialog(null, "Designer Iválido!", "", 2);
                    } else {
                        if ("".equals(cb_cliente.getSelectedItem().toString())) {
                            JOptionPane.showMessageDialog(null, "Cliente Iválido!", "", 2);
                        } else {
                            if ("".equals(cb_status.getSelectedItem().toString())) {
                                JOptionPane.showMessageDialog(null, "Status Iválido!", "", 2);
                            } else {

                                Utilitarios dh = new Utilitarios();
                                String dataHora = dh.DH();

                                int idcliente = ApontaIdCliente();
                                int idDesigner = ApontaIdDesigner();
                                Date dataSelecionada = jd_data.getDate();
                                // Formata a data como YYYY/MM/DD
                                SimpleDateFormat formato = new SimpleDateFormat("yyyy/MM/dd");
                                String dataFormatada = formato.format(dataSelecionada);

                                Indicador obj = new Indicador();

                                obj.setPedido(txt_pedido.getText());
                                obj.setQtd_artes(Integer.parseInt(txt_numArtes.getText()));
                                obj.setId_cliente(idcliente);
                                obj.setId_funcionario(idDesigner);
                                obj.setStatus(cb_status.getSelectedItem().toString());
                                obj.setData(dataFormatada);
                                obj.setCriado(dataHora);
                                obj.setModificado(dataHora);
                                //JOptionPane.showMessageDialog(null, dataFormatada, "", 2);

                                IndicadorDAO dao = new IndicadorDAO();
                                dao.cadastrarIndicador(obj);

                                this.dispose();

                            }
                        }
                    }
                }
            }
        }


    }//GEN-LAST:event_jLabel2MouseClicked

