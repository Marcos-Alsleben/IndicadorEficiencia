//Metodo Atualiza cb_cliente
    public void AtualizarcbClientes() {
        ClienteDAO dao = new ClienteDAO();
        List<Cliente> lista = dao.listarCliente();

        cb_cliente.removeAllItems();
        cb_cliente.addItem("");

        for (Cliente c : lista) {
            cb_cliente.addItem(c.getNome());

        }
    }

//Metodo Atualiza cb_designer
    public void AtualizarcbDesigner() {
        FuncionarioDAO dao = new FuncionarioDAO();
        List<Funcionario> lista = dao.listarFuncionario();

        cb_designer.removeAllItems();
        cb_designer.addItem("");

        for (Funcionario c : lista) {
            cb_designer.addItem(c.getNome());

        }
    }

//Metodo Aponta id Cliente
    public int ApontaIdCliente() {
        int id = 0;

        ClienteDAO dao = new ClienteDAO();
        List<Cliente> lista = dao.RetornaId_cliente(cb_cliente.getSelectedItem().toString());

        for (Cliente c : lista) {
            id = c.getId_cliente();

        }
        return id;
    }

//Metodo Aponta id Designer
    public int ApontaIdDesigner() {
        int id = 0;

        FuncionarioDAO dao = new FuncionarioDAO();
        List<Funcionario> lista = dao.RetornaId(cb_designer.getSelectedItem().toString());

        for (Funcionario c : lista) {
            id = c.getId_funcionario();

        }
        return id;
    }

